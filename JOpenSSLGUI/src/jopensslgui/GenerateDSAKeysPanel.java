    private void generateParamButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_generateParamButtonActionPerformed
    {//GEN-HEADEREND:event_generateParamButtonActionPerformed
        try
        {
            StringBuilder cmdBuilder = new StringBuilder("openssl dsaparam -out ");
            cmdBuilder.append(paramOutputFileField.getText());
            cmdBuilder.append(" " + (String) sizeComboBox.getSelectedItem());
            Process p = Runtime.getRuntime().exec(cmdBuilder.toString());
            p.waitFor();
        }
        catch (InterruptedException ex)
        {
            Logger.getLogger(GenerateDSAKeysPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (IOException ex)
        {
            Logger.getLogger(GenerateDSAKeysPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_generateParamButtonActionPerformed

