    private static void processLiteralData(PGPLiteralData ld, OutputStream out, PGPOnePassSignature ops) throws IOException, SignatureException, SignatureException{
        InputStream unc = ld.getInputStream();
        int ch;
        if(ops==null){
            while ((ch = unc.read()) >= 0) 
                out.write(ch);
        }else{
            while ((ch = unc.read()) >= 0) {
                out.write(ch);
                ops.update((byte)ch);
            }
        }
    }
